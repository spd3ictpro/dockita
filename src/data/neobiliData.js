function lookup(ga, hours, table) {
  const gaRow = table.find(r => r.ga === ga)
  if (!gaRow) return null

  const keys = Object.keys(gaRow).filter(k => k !== 'ga').map(Number).sort((a, b) => a - b)
  if (hours <= keys[0]) return gaRow[keys[0]]
  if (hours >= keys[keys.length - 1]) return gaRow[keys[keys.length - 1]]

  let lo = keys[0], hi = keys[keys.length - 1]
  for (let i = 0; i < keys.length - 1; i++) {
    if (hours >= keys[i] && hours <= keys[i + 1]) {
      lo = keys[i]; hi = keys[i + 1]; break
    }
  }
  const vLo = gaRow[lo], vHi = gaRow[hi]
  return vLo + (vHi - vLo) * (hours - lo) / (hi - lo)
}

